export function TechStack() {
  const sectionRef = useRef<HTMLElement>(null)
  const [isVisible, setIsVisible] = useState(false)

  useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true)
          observer.disconnect()
        }
      },
      { threshold: 0.15 }
    )

    if (sectionRef.current) {
      observer.observe(sectionRef.current)
    }

    return () => observer.disconnect()
  }, [])

  return (
    <section
      ref={sectionRef}
      className="px-4 sm:px-6 py-20 sm:py-28 border-t border-border/30"
    >
      <div className="mx-auto max-w-4xl">
        <div
          className="mb-12 sm:mb-16 space-y-3 transition-all duration-700"
          style={{
            opacity: isVisible ? 1 : 0,
            transform: isVisible ? "translateY(0)" : "translateY(24px)",
          }}
        >
          <p className="font-mono text-xs uppercase tracking-[0.25em] sm:tracking-[0.35em] text-primary">
            Technology Stack
          </p>
          <h2 className="text-3xl font-bold tracking-tight sm:text-4xl lg:text-5xl">How I Build</h2>
          <p className="max-w-2xl text-base sm:text-lg text-muted-foreground leading-relaxed">
            Every layer serves a purpose in the delivery pipeline. I choose tools based on the problem they solve, not the trend they follow — prioritizing maintainability, security, and production reliability at each tier.
          </p>
        </div>

        <div className="space-y-10 sm:space-y-14">
          {layers.map((layer, index) => (
            <div
              key={layer.title}
              className="transition-all duration-700"
              style={{
                opacity: isVisible ? 1 : 0,
                transform: isVisible ? "translateY(0)" : "translateY(24px)",
                transitionDelay: `${index * 120 + 200}ms`,
              }}
            >
              <div className="mb-3 space-y-1.5">
                <h3 className="text-lg font-semibold tracking-tight sm:text-xl">{layer.title}</h3>
                <p className="text-sm leading-relaxed text-muted-foreground">{layer.responsibility}</p>
              </div>

              <div className="flex flex-wrap gap-x-1 gap-y-0.5" role="list" aria-label={`${layer.title} technologies`}>
                {layer.technologies.map((tech) => (
                  <TechItem key={tech.name} item={tech} />
                ))}
              </div>

              {index < layers.length - 1 && (
                <div className="mt-10 sm:mt-14 flex justify-center">
                  <div className="h-8 w-px bg-border/40" />
                </div>
              )}
            </div>
          ))}
        </div>
      </div>
    </section>
  )
}
